const fs = require("fs");
const path = require("path");

const PROJECT_ROOT = path.resolve(__dirname, "..");
const SOURCE_PATH = path.join(PROJECT_ROOT, "data", "vocabulary.enriched.json");
const OUTPUT_PATH = path.join(PROJECT_ROOT, "js", "vocabulary.js");
const VOCABULARY_SET_ID = "g6-p1-p2";
const REQUIRED_ENTRY_FIELDS = [
  "no",
  "word",
  "part_of_speech",
  "meaning_zh",
  "englishDefinition",
  "exampleSentence",
  "phrase",
  "phonicsHint"
];

function main() {
  const source = readJson(SOURCE_PATH);
  const rows = flattenVocabulary(source);
  validateSource(source, rows);
  writeVocabularyJs(source, rows);
  console.log(`Generated ${path.relative(PROJECT_ROOT, OUTPUT_PATH)} with ${rows.length} words.`);
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function flattenVocabulary(source) {
  if (!Array.isArray(source.categories)) {
    throw new Error("Expected data/vocabulary.enriched.json to contain a categories array.");
  }

  return source.categories.flatMap(function (category) {
    if (!Array.isArray(category.entries)) {
      throw new Error(`Category ${category.category_en || "(unknown)"} has no entries array.`);
    }

    return category.entries.map(function (entry) {
      return {
        vocabularySetId: VOCABULARY_SET_ID,
        wordId: createWordId(category, entry),
        audioBaseName: createAudioBaseName(category, entry),
        sourceNo: entry.no,
        word: entry.word,
        partOfSpeech: entry.part_of_speech,
        chineseMeaning: entry.meaning_zh,
        categoryZh: category.category_zh,
        categoryEn: category.category_en,
        englishDefinition: entry.englishDefinition,
        exampleSentence: entry.exampleSentence,
        phrase: entry.phrase,
        phonicsHint: entry.phonicsHint
      };
    });
  });
}

function createAudioBaseName(category, entry) {
  return [
    slugify(category.category_en || "uncategorized"),
    String(entry.no).trim(),
    slugify(entry.word)
  ].join("-");
}

function createWordId(category, entry) {
  return [
    VOCABULARY_SET_ID,
    slugify(category.category_en || "uncategorized"),
    String(entry.no).trim(),
    slugify(entry.word)
  ].join(":");
}

function slugify(value) {
  return String(value || "")
    .trim()
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function validateSource(source, rows) {
  if (Number(source.total_count) !== rows.length) {
    throw new Error(`total_count mismatch: source says ${source.total_count}, generated ${rows.length}.`);
  }

  const seenWords = new Set();
  const seenWordIds = new Set();
  source.categories.forEach(function (category) {
    const entries = category.entries || [];
    if (Number(category.count) !== entries.length) {
      throw new Error(`${category.category_en} count mismatch: source says ${category.count}, entries ${entries.length}.`);
    }

    entries.forEach(function (entry) {
      REQUIRED_ENTRY_FIELDS.forEach(function (fieldName) {
        if (entry[fieldName] === undefined || entry[fieldName] === null || String(entry[fieldName]).trim() === "") {
          throw new Error(`Missing ${fieldName} for entry no ${entry.no || "(unknown)"}.`);
        }
      });

      const wordKey = String(entry.word).trim().toLowerCase();
      if (seenWords.has(wordKey)) {
        throw new Error(`Duplicate word: ${entry.word}.`);
      }
      seenWords.add(wordKey);

      const wordId = createWordId(category, entry);
      if (seenWordIds.has(wordId)) {
        throw new Error(`Duplicate wordId: ${wordId}.`);
      }
      seenWordIds.add(wordId);

      const exampleWordCount = countEnglishWords(entry.exampleSentence);
      if (exampleWordCount < 7) {
        throw new Error(`Example sentence for ${entry.word} has ${exampleWordCount} English words; expected at least 7.`);
      }
    });
  });
}

function countEnglishWords(sentence) {
  return String(sentence)
    .split(/[^A-Za-z']+/)
    .filter(Boolean)
    .length;
}

function writeVocabularyJs(source, rows) {
  const output = [
    "// Generated by: scripts/generate-vocabulary-js.js",
    "// Source: data/vocabulary.enriched.json",
    `// Vocabulary set: ${VOCABULARY_SET_ID}`,
    `// Source total_count: ${source.total_count}; generated_count: ${rows.length}`,
    `window.VOCABULARY_DATA = ${JSON.stringify(rows, null, 2)};`,
    ""
  ].join("\n");

  fs.writeFileSync(OUTPUT_PATH, output, "utf8");
}

main();
